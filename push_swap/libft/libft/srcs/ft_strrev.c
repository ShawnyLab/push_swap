#include "libft.h"

char	*ft_strrev(char *str)
{
	int		count;
	int		i;
	char	c;

	count = (int)ft_strlen((char*)str);
	count = count - 1;
	i = 0;
	while (i < ((count + 1) / 2))
	{
		c = str[i];
		str[i] = str[count - i];
		str[count - i] = c;
		i++;
	}
	return (str);
}
