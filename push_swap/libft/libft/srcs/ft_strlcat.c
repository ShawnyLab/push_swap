#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	j;
	size_t	a;

	i = ft_strlen(dst);
	j = ft_strlen((char*)src);
	if (dstsize <= i)
		a = dstsize + j;
	else
		a = j + i;
	j = 0;
	while (src[j] != '\0' && i + 1 < dstsize)
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	dst[i] = '\0';
	return (a);
}
